export function convertHalfString(str: string) {
	var fullStr = str;
	for (var i = 0; i < beforeStr.length; i++) {
		fullStr = fullStr.replace(new RegExp(beforeStr[i], "g"), afterStr[i]);
	}
	return fullStr;
}

function tr(text: string, search: string, replace: string) {
	// Make the search string a regex.
	var regex = RegExp("[" + search + "]", "g");
	var t = text.replace(regex, function (chr) {
		// Get the position of the found character in the search string.
		var ind = search.indexOf(chr);
		// Get the corresponding character from the replace string.
		var r = replace.charAt(ind);
		return r;
	});
	return t;
}

export function converJpNumbers(number: string) {
	return tr(number, "０１２３４５６７８９　", "0123456789 ");
}

export function convertNormal(text?: string) {
	return converJpNumbers(convertHalfString(text || ""));
}
